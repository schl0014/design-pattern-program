public class HeadPhones {
    public void on() {
        System.out.println("Headphones is on");
    }

    public void VolumeUp(int volume) {
        System.out.println("Volume increased by" + volume);
    }

    public void VolumeDown(int volume) {
        System.out.println("Volume increased by" + volume);
    }

    public void off() {
        System.out.println("Headphones is off");
    }

    public void connection() {
        System.out.println("Headphones are connected");
    }
}
