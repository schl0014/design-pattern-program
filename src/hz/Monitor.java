public class Monitor {

    public void BrightnessUp(int brightness){
        System.out.println("Brightness increased by"+ brightness);
    }
    public void BrightnessDown(int brightness){
        System.out.println("Brightness increased down"+ brightness);
    }

    public void on(){
        System.out.println("Monitor is on");
    }


    public void off(){
        System.out.println("Monitor is off");
    }

    public void Colorpallet(){
            System.out.println("Color pallet has changed");
    }
}
